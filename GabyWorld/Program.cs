using Microsoft.AspNetCore;

namespace GabyWorld
{
    public class Program
    {

        public static void Main(string[] args)
        {
            BuildWebHost(args).Run();
        }

        public static IWebHost BuildWebHost(string[] args)
        {
            return WebHost.CreateDefaultBuilder().
                UseStartup<Startup>()
                .Build();
        }
    }
}